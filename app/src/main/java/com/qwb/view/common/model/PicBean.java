package com.qwb.view.common.model;



/**
 * 图片共用
 * 小图：picMin,picMini
 * 大图：pic
 */
public class PicBean{
	private String nm;
	private String pic;
	private String picMin;
	private String picMini ;

	public String getNm() {
		return nm;
	}

	public void setNm(String nm) {
		this.nm = nm;
	}

	public String getPic() {
		return pic;
	}

	public void setPic(String pic) {
		this.pic = pic;
	}

	public String getPicMin() {
		return picMin;
	}

	public void setPicMin(String picMin) {
		this.picMin = picMin;
	}

	public String getPicMini() {
		return picMini;
	}

	public void setPicMini(String picMini) {
		this.picMini = picMini;
	}
}
